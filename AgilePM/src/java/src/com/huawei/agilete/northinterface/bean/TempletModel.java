package com.huawei.agilete.northinterface.bean;

public class TempletModel {

	private String name;
	private String id;
	private String value;
	private String remark;
	private String mrtgUse;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getMrtgUse() {
		return mrtgUse;
	}
	public void setMrtgUse(String mrtgUse) {
		this.mrtgUse = mrtgUse;
	}
	
	
}
